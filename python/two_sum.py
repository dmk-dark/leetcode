def two_sum(nums: list[int], target: int) -> list[int]:
    """
    Функция для поиска двух чисел в массиве, которые в сумме дают заданное целевое число.

    Аргументы:
    nums: list[int] - список чисел, в котором нужно найти пару чисел.
    target: int - целевое число, которое должна давать сумма двух чисел.

    Возвращает:
    list[int] - список из двух индексов найденных чисел. Если пара не найдена, возвращает пустой список.

    Сложность:
    O(n), где n - это количество элементов в списке
    """
    num_map = {}  # Создаем словарь для хранения чисел и их индексов
    for i, num in enumerate(nums):  # Проходим по всем числам в списке
        complement = target - num  # Вычисляем дополнение до целевого числа
        if complement in num_map:  # Если дополнение уже в словаре, значит мы нашли пару
            return [num_map[complement], i]  # Возвращаем индексы пары
        num_map[num] = i  # Добавляем текущее число и его индекс в словарь
    return []  # Если пара не найдена, возвращаем пустой список